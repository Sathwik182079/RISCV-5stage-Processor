//-----------------------------------------------------
// Description:
// Testbench for ALU Control Unit
//
// Verifies:
// • Load Instructions
// • Branch Instructions
// • ADD
// • SUB
// • AND
// • OR
// • XOR
// • SLL
// • SRL
// • SRA
// • SLT
// • Default Case
//-----------------------------------------------------

`timescale 1ns/1ps

module alu_control_tb;

reg [1:0] ALUOp;
reg [2:0] funct3;
reg [6:0] funct7;

wire [3:0] ALUControl;

alu_control dut(

    .ALUOp(ALUOp),
    .funct3(funct3),
    .funct7(funct7),
    .ALUControl(ALUControl)

);

initial
begin

    $monitor(
    "Time=%0t ALUOp=%b funct3=%b funct7=%b ALUControl=%b",
    $time,
    ALUOp,
    funct3,
    funct7,
    ALUControl
    );

end

initial
begin

    //-------------------------------------------------
    // Load / Store
    //-------------------------------------------------
    ALUOp  = 2'b00;
    funct3 = 3'b000;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // Branch
    //-------------------------------------------------
    ALUOp  = 2'b01;
    funct3 = 3'b000;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // ADD
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b000;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // SUB
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b000;
    funct7 = 7'b0100000;
    #10;

    //-------------------------------------------------
    // AND
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b111;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // OR
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b110;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // XOR
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b100;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // SLL
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b001;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // SRL
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b101;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // SRA
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b101;
    funct7 = 7'b0100000;
    #10;

    //-------------------------------------------------
    // SLT
    //-------------------------------------------------
    ALUOp  = 2'b10;
    funct3 = 3'b010;
    funct7 = 7'b0000000;
    #10;

    //-------------------------------------------------
    // Invalid ALUOp
    //-------------------------------------------------
    ALUOp  = 2'b11;
    funct3 = 3'b000;
    funct7 = 7'b0000000;
    #10;

    $finish;

end

endmodule